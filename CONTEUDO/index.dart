import 'dart:math';

void main() {
  var posicao = 2;
  switch (posicao) {
    case 1:
      {
        print('Primeiro Lugar');
      }
      break;
    case 2:
      {
        print('Segundo Lugar');
      }
      break;
    case 3:
      {
        print('Terceiro Lugar');
      }
      break;
    case 4:
      {
        print('Quarto Lugar');
      }
      break;
    default:
      {
        print('Não encontramos a sua posição!');
      }
      break;
  }
}
